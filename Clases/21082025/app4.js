// Ejemplos básicos de console.log mostrando distintos tipos de datos
console.log("Hola mundo"); // Imprime un texto (string)
console.log(true);        // Imprime un valor booleano true
console.log(false);       // Imprime un valor booleano false
console.log(20);          // Imprime un número entero
console.log(1.2);         // Imprime un número decimal (float)
console.log(1+1);         // Imprime el resultado de una operación aritmética (2)
